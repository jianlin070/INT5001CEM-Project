    public void fetchDataToTable(){
         try{
            java.sql.Date sqlDate = new java.sql.Date(Calendar.getInstance().getTime().getTime());
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/access_control_ms","root","");
            String sql = "select date_register, time_register,tic_no, date_visit, time_visit, reason FROM request WHERE visitor_ic = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            
            pst.setString(1, ic_no);
            
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                
                Date date_register = rs.getDate("date_register");
                Date time_register = rs.getTime("time_register");
                tic_no = rs.getString("tic_no");
                Date date_visit = rs.getDate("date_visit");
                Date time_visit = rs.getTime("time_visit");
                String reason = rs.getString("reason");
              
                Object[] obj = {date_register, time_register, tic_no, date_visit, time_visit, reason};
                model = (DefaultTableModel) tbl_request.getModel();
                model.addRow(obj);
            }
            
        } catch (Exception e){
            e.printStackTrace();
        }
    }

